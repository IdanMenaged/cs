
public class Dice
{
    
    private int roll1;  // first roll
    private int roll2;  // second roll
    
    // constructor
    public Dice(int roll1, int roll2)
    {
        this.roll1 = roll1;
        this.roll2 = roll2;
    }

    // getters
    public int getRoll1()
    {
        return this.roll1;
    }
    
    public int getRoll2()
    {
        return this.roll2;
    }    

}
